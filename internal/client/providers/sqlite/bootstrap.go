// Package sqlite предоставляет низкоуровневые ИБ-драйверы, миграции и репозитории
// для управления зашифрованным локальным хранилищем СУБД SQLite.
package sqlite

import (
	"database/sql"
	"fmt"
	"log/slog"
)

// Bootstrap выполняет полный сквозной цикл разворачивания локального крипто-контейнера.
//
// Функция открывает физический файл базы данных, валидирует права доступа (0600/0700),
// настраивает транзакционный режим WAL и накатывает SQL-миграции схемы таблиц.
// В случае сбоя гарантирует атомарное закрытие дескрипторов ресурсов без утечек в ОС.
func Bootstrap(path string) (*sql.DB, error) {
	slog.Debug("Инициирована процедура первичного разворачивания (bootstrap) контейнера СУБД")

	// Открываем существующий или создаем новый файл БД с проверкой ИБ-прав доступа
	db, err := Open(path)
	if err != nil {
		slog.Error("Не удалось выполнить физическое открытие файла контейнера СУБД", "error", err)
		return nil, fmt.Errorf("открытие контейнера SQLite: %w", err)
	}

	// Запускаем бесшумный накат схемы миграций (таблицы device_state и records)
	if err := Migrate(db); err != nil {
		slog.Error("Аварийное прерывание наката SQL-схемы миграций, запуск очистки дескрипторов", "error", err)

		// Явно перехватываем ошибку закрытия пула для исключения утечек в операционной системе
		if closeErr := db.Close(); closeErr != nil {
			slog.Error("Критическая ошибка: деструктор пула СУБД завершился сбоем при аварийном выходе", "close_error", closeErr)
			return nil, fmt.Errorf("миграция СУБД провалена (%w), каскадный сбой закрытия дескриптора файла: %w", err, closeErr)
		}

		return nil, fmt.Errorf("миграция структуры таблиц СУБД: %w", err)
	}

	slog.Debug("Локальный крипто-контейнер хранения успешно создан, схема таблиц верифицирована")
	return db, nil
}
